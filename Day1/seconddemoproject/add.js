module.exports.add=(a,b)=>{
    return a+b;
}

module.exports.subtract=(a,b)=>{
    return a-b;
}

const product=(a,b)=>{
    return a*b;
}

module.exports.multiply=(a,b)=>{
    return product(a,b);
}
